public class StringExercise {
    public static void main(String[] args){
        String education = "We don't need no education";
        System.out.println(education);

        String thoughControl = "We don't need no thought control";
        System.out.println(thoughControl);

        String check = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(check);

        String windows = "In windows, the main drive is usually C:\\ ";
        System.out.println(windows);

        String backlash = "I can do backslashes \\, double backslashes\\\\, and the amazing triple backslash \\\\\\!";
        System.out.println(backlash);
    }
}
